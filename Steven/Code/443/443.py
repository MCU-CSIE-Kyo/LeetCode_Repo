class Solution:
    def compress(self, chars: list[str]) -> int:
        count_list = []
        chars_sort = sorted(chars)
        char_dict = {char:0 for char in chars_sort}

        for chat in chars:
            char_dict[chat] += 1
        for key, value in char_dict.items():
            if value == 1:
                count_list.append(key)
            else:
                count_list.append(key)
                inter_list = list(str(value))
                for inter in inter_list:
                    count_list.append(str(inter))

        return count_list

